/*
    Estrutura Condicional: "if" (se)
    
    A if(...condição) instrução avalia uma condição entre parênteses e, 
    se o resultado for true, executa um bloco de código.
*/

let ano = 2022;

if (ano === 2022) { // ano === 2022 é igual a "true", então executará o bloco de código
    console.log("Estamos em 2022!");
}