/*
    * Relacionais -> Possuem como resposta boolean (true/false)
*/

console.log('1)', '1' == 1) // igual
console.log('2)', '1' === 1) // estritamente igual (considera também o tipo)
console.log('3)', '3' != 3) // diferente
console.log('4)', '3' !== 3) // estritamente diferente (considera também o tipo)

console.log('5)', 3 < 2) // menor
console.log('6)', 3 > 2) // maior
console.log('7)', 3 <= 2) // menor ou igual
console.log('8)', 3 >= 2) // maior ou igual

console.log('9)', null == undefined) 
console.log('10)', null === undefined)
console.log('11)', null !== undefined)
